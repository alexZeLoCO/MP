	public static void add (Entity a) {
		getMap()[a.getI()][a.getJ()]=a.getType();
	}
	
	public static void change (int [] a, int [] b) {
		int memory = getMap()[a[0]][a[1]];
		getMap()[a[0]][a[1]]=getMap()[b[0]][b[1]];
		getMap()[b[0]][b[1]]=memory;
	}
	
	public static void movement (int d, Entity e) {
		int [] np ={e.getI(),e.getJ()};
		if (e.possibleMove(d)) {
			switch (d) {
			case 2:
				np[0]=e.getI()+1;
				break;
			case 4:
				np[1]=e.getJ()-1;
				break;
			case 8:
				np[0]=e.getI()-1;
				break;
			case 6:
				np[1]=e.getJ()+1;
				break;
			}
			change(e.getCoordinates(),np);
		}
	}
	
